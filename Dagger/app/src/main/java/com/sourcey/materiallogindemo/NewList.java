package com.sourcey.materiallogindemo;

public class NewList {

    String email;
    String id;

    public NewList() {
    }

    public NewList(String email, String id ) {
        id = id;
        email = email;
    }

    public String getid() {
        return id;
    }

    public String getemail() {
        return email;
    }
}
